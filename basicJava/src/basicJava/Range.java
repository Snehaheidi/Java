package basicJava;

public class Range {
	public static void main(String[] args) {
	System.out.println("Int Size = "+Integer.SIZE+",\tRange ="+Integer.MIN_VALUE+" to "+Integer.MAX_VALUE);
	System.out.println("Byte Size = "+Byte.SIZE+",\tRange ="+Byte.MIN_VALUE+" to "+Byte.MAX_VALUE);
	System.out.println("Char Size = "+Character.SIZE+",\tRange ="+Character.MIN_VALUE+" to "+Character.MAX_VALUE);
	System.out.println("Float Size = "+Float.SIZE+",\tRange ="+Float.MIN_VALUE+" to "+Float.MAX_VALUE);
	System.out.println("Double Size = "+Double.SIZE+",\tRange ="+Double.MIN_VALUE+" to "+Double.MAX_VALUE);
	System.out.println("Long Size  = "+Long.SIZE+",\tRange ="+Long.MIN_VALUE+" to "+Long.MAX_VALUE);
	}
}
